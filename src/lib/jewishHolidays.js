// Build a fast lookup: 'YYYY-MM-DD' → [{ name, icon }, ...]
const holidayMap = {};
for (const [date, name, icon] of HOLIDAY_DB) {
  if (!holidayMap[date]) holidayMap[date] = [];
  holidayMap[date].push({ name, icon });
}

/**
 * Returns holidays for a given Gregorian year + month (0-indexed).
 * Result: Map<dayOfMonth (number), [{name, icon}]>
 */
export function getHolidaysForMonth(year, month) {
  const result = {};
  const prefix = `${year}-${String(month + 1).padStart(2, '0')}-`;
  for (const [date, , ] of HOLIDAY_DB) {
    if (!date.startsWith(prefix)) continue;
    const day = parseInt(date.slice(8), 10);
    if (!result[day]) result[day] = [];
    const list = holidayMap[date];
    if (list) result[day].push(...list);
  }
  return result;
}

/**
 * Returns holidays for a specific date string (YYYY-MM-DD).
 */
export function getHolidaysForDate(dateStr) {
  return holidayMap[dateStr] ?? [];
}
